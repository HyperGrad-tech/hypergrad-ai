export default function PromptLibrary() {
  const [cat, setCat] = useState('全部');
  const [expanded, setExpanded] = useState<string | null>(null);
  const [copied, setCopied] = useState<string | null>(null);

  const list = cat === '全部' ? PROMPTS : PROMPTS.filter(p => p.category === cat);

  const copy = useCallback((title: string, text: string) => {
    navigator.clipboard.writeText(text);
    setCopied(title);
    setTimeout(() => setCopied(null), 1500);
  }, []);

  return (
    <div>
      <div class="tool-card" style={{ background: 'var(--bg-soft)' }}>
        <div class="text-sm font-bold" style={{ marginBottom: '8px' }}>📚 {PROMPTS.length} 个精选 Prompt，覆盖 {CATEGORIES.length - 1} 大场景</div>
        <div class="text-xs text-muted">点击卡片展开完整 Prompt，一键复制后粘贴到任意 AI 对话框（ChatGPT / DeepSeek / Claude / GLM 通用）。把 {'{{变量}}'} 替换为你的真实内容即可。</div>
      </div>

      {/* 分类筛选 */}
      <div style={{ display: 'flex', flexWrap: 'wrap', gap: '6px', margin: '16px 0 12px' }}>
        {CATEGORIES.map(c => (
          <button
            onClick={() => setCat(c)}
            style={{
              padding: '5px 14px', fontSize: '13px', borderRadius: '16px', cursor: 'pointer',
              border: cat === c ? '1px solid var(--blue)' : '1px solid var(--border)',
              background: cat === c ? 'var(--blue)' : '#fff',
              color: cat === c ? '#fff' : 'var(--text)',
              fontWeight: cat === c ? 600 : 400,
            }}
          >{c}{c !== '全部' && ` ${PROMPTS.filter(p => p.category === c).length}`}</button>
        ))}
      </div>

      {/* Prompt 卡片 */}
      <div>
        {list.map(p => {
          const key = p.category + '|' + p.title;
          const isOpen = expanded === key;
          return (
            <div class="tool-card" key={key} style={{ marginBottom: '10px' }}>
              <div
                onClick={() => setExpanded(isOpen ? null : key)}
                style={{ cursor: 'pointer' }}
              >
                <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'flex-start', gap: '10px' }}>
                  <div style={{ flex: 1 }}>
                    <div style={{ display: 'flex', alignItems: 'center', gap: '8px', marginBottom: '4px' }}>
                      <span class="text-sm font-bold">{p.title}</span>
                      <span class="tag tag-p2" style={{ fontSize: '10px' }}>{p.category}</span>
                    </div>
                    <div class="text-xs text-muted">{p.desc}</div>
                  </div>
                  <span style={{ color: 'var(--text-muted)', fontSize: '13px' }}>{isOpen ? '▲' : '▼'}</span>
                </div>
              </div>
              {isOpen && (
                <div style={{ marginTop: '12px' }}>
                  <pre style={{
                    background: 'var(--bg-soft)', padding: '12px', borderRadius: 'var(--radius-sm)',
                    fontFamily: 'var(--font-mono)', fontSize: '12px', lineHeight: 1.6,
                    whiteSpace: 'pre-wrap', wordBreak: 'break-word', color: 'var(--text)',
                    margin: 0, border: '1px solid var(--border)',
                  }}>{p.prompt}</pre>
                  <button
                    class="btn btn-ghost btn-sm"
                    style={{ marginTop: '8px' }}
                    onClick={() => copy(p.title, p.prompt)}
                  >{copied === p.title ? '✓ 已复制' : '复制 Prompt'}</button>
                </div>
              )}
            </div>
          );
        })}
      </div>
    </div>
  );
}
